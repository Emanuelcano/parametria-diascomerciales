     * Create or Update a new record in the DB
     * @param stdClass $object
     * @param int $chatID
     * @param bool $returnDate
     * @param bool $registerMedia
     * @return string|bool
     * @throws GuzzleException
     */
    public function modelCreateDb(stdClass $object, int $chatID, bool $returnDate = false,bool $registerMedia = true)
    {
        $creationDate = Carbon::now()
                              ->format('Y-m-d H:i:s.u');
        $data         = [
            'id_chat'             => $chatID,
            'sms_message_sid'     => $object->SmsMessageSid,
            'num_media'           => $object->NumMedia,
            'sms_status'          => $object->SmsStatus,
            'body'                => $object->Body,
            'num_segments'        => $object->NumSegments,
            'media_content_type0' => isset($object->MediaContentType0) ?
                str_replace('\\', '', $object->MediaContentType0) : null,
            'media_url0'          => isset($object->MediaUrl0) ? str_replace('\\', '', $object->MediaUrl0) : null,
            'api_version'         => $object->ApiVersion,
            'fecha_creacion'      => $creationDate
        ];

        $this->db->trans_start();
        $this->db->trans_strict(FALSE);
        $this->db->insert('chat.' . $this->tableName, $data);
        $lastID = $this->db->insert_id();

        if ($this->db->trans_status() === FALSE) {
            $this->db->trans_rollback();

            return false;
        }

        $this->db->trans_commit();

        if ($object->NumMedia > 0 && $registerMedia) {
            $this->registerMedias($lastID, $object);
        }

        if ($returnDate) {
            return $creationDate;
        }

        return true;
    }

    /**
     * @param mixed $provider
     */
    public function setProvider($provider): void
    {
        $this->provider = $provider;
    }

    /**
     * @param int $lastID
     * @param stdClass $object
     * @throws GuzzleException
     */
    private function registerMedias(int $lastID, stdClass $object): void
    {
        $numMedia = (int)$object->NumMedia;

        for ($idx = 0; $idx < $numMedia; $idx++) {
            $mediaUrl  = str_replace('\\', '', $object->{'MediaUrl' . $idx});
            $mimeType  = str_replace('\\', '', $object->{'MediaContentType' . $idx});
            $pathParts = explode('/', parse_url($mediaUrl)['path']);
            $mediaSid  = end($pathParts);
            $fileExt   = $this->mime2ext($mimeType);
            $client    = new GuzzleHttp\Client();
            $fileRoute = FCPATH . 'media/chat_medias/' . $mediaSid . '.' . $fileExt;
            $client->request('GET', $mediaUrl, ['sink' => $fileRoute]);

            $this->load->model('subresourceuris');
            /** @var Subresourceuris $mediaResourceModel */
            $mediaResourceModel = $this->subresourceuris;
            $mediaResourceModel->modelCreateDb(
                [
                    'cod_message_sid' => $lastID,
                    'media'           => '/media/chat_medias/' . $mediaSid . '.' . $fileExt
                ]
            );
        }
    }

